class Solution{
    public:
    // arr[] : int input array of integers
    // k : the quadruple sum required
    vector<vector<int> > fourSum(vector<int> &arr, int k) {
        // Your code goes here
        vector<vector<int> > ans;
        int n = arr.size();
        for(int i=0;i<n;i++){
            for(int j=i+1;j<n;j++){
                for(int k=j+1;k<n;k++){
                    for(int l=k+1;l<n;l++){
                        int sum = arr[i]+arr[j]+arr[k]+arr[l];
                        if(sum==k){
                            cout<<sum<<" ";
                        }
                    }
                }
            }
        }
        return ans;
    }
};

